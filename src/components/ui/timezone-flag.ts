/** Convert a 2-letter ISO 3166-1 alpha-2 country code to its flag emoji. */
export function toFlag(countryCode: string): string {
  return countryCode
    .toUpperCase()
    .replace(/./g, (char) => String.fromCodePoint(127397 + char.charCodeAt(0)));
}

