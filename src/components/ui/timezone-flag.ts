/**
 * Format an IANA timezone string as "🇸🇮 Ljubljana".
 * Falls back to the raw city segment if the timezone is not in the map.
 */
export function formatTimezone(tz: string): string {
  const city = tz.split("/").pop()?.replace(/_/g, " ") ?? tz;
  const countryCode = TIMEZONE_COUNTRY[tz];
  if (!countryCode) return city;
  return `${toFlag(countryCode)} ${city}`;
}